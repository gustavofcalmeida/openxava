package org.openxava.annotations;

/**
 * Types of charts supported by the @Chart annotation.
 * 
 * @since 7.5
 * @author Javier Paniza
 */
public enum ChartType {
    /**
     * Bar chart (default)
     */
    BAR,
    
    /**
     * Line chart
     */
    LINE,
    
    /**
     * Pie chart
     */
    PIE
}
